<?php

// Control Flow!!

// for
echo "For Loop: <br>";
for ($i = 0; $i < 5; $i++) {
    echo "Hello World (for) <br>";
}

echo "<br>";

// while
echo "While Loop: <br>";
$i = 0; // Re-assign

while ($i < 5) {
    echo "Hello World (while) <br>";
    $i++;
}

echo "<br>";

// do-while
echo "Do-while Loop: <br>";
$i = 0; //Re-assign

do {
    echo "Hello World (do) <br>";
    $i++;
} while ($i < 5);


